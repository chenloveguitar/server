package com.magicmoble.luzhouapp.json.core;

import java.util.List;

public class ListObject {
	private List<?> result;
	private int count;

	public List<?> getResult() {
		return this.result;
	}

	public void setResult(List<?> result) {
		this.result = result;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}
